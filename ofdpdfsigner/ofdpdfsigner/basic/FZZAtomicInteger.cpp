#include "basic/FZZConst.h"
#include "basic/FZZAtomicInteger.h"
//-----------------------------------------------------------------------------------------------------------------
/***********************************************FZZAtomicInteger**************************************************************/
//-----------------------------------------------------------------------------------------------------------------
FZZAtomicInteger::FZZAtomicInteger() : m_munber(0)
{
    //DLOG("Font()");
    FZZConst::addClassCount("FZZAtomicInteger");
    
}
//-----------------------------------------------------------------------------------------------------------------
FZZAtomicInteger::FZZAtomicInteger(long initialValue) :FZZAtomicInteger()
{
    m_munber = initialValue;
}
//-----------------------------------------------------------------------------------------------------------------
FZZAtomicInteger::FZZAtomicInteger(const FZZAtomicInteger& obj): FZZAtomicInteger()
{
    if ( this == &obj ) {
        return;
    }
    this->m_munber = obj.m_munber;
}
//-----------------------------------------------------------------------------------------------------------------
const FZZAtomicInteger& FZZAtomicInteger::operator=(const FZZAtomicInteger& obj)
{
    if ( this == &obj ) {
        return *this;
    }
    this->m_munber = obj.m_munber;
    return *this;
}
//-----------------------------------------------------------------------------------------------------------------
FZZAtomicInteger::~FZZAtomicInteger()
{
    //DLOG("~Font()");
    FZZConst::delClassCount("FZZAtomicInteger");
   
}
