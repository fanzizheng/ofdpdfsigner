import Foundation


public class ColorType {
    public var red: Double = 0
    public var green: Double = 0
    public var blue: Double = 0
    public var alpha: Double = 0
    
    public init(red: Double = 0,
                green: Double = 0,
                blue: Double = 0,
                alpha: Double = 0) {
        self.red = red
        self.green = green
        self.blue = blue
        self.alpha = alpha
        
    }
}
